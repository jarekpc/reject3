CREATE TABLE `EVENTS`(
	`ID_EVENT` INTEGER NOT NULL  AUTO_INCREMENT,
	`TITLE` varchar(255) NOT NULL,
	`STARTSAT`DATETIME DEFAULT NULL,
	`ENDSAT`  DATETIME DEFAULT NULL,
	`COST` INTEGER,
	PRIMARY KEY(`ID_EVENT`)
)
