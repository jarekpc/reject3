CREATE TABLE `CLIENT` (
	`ID_EVENT` INTEGER NOT NULL  AUTO_INCREMENT,
	`NAME` VARCHAR(255) NOT NULL,
	`SURNAME` VARCHAR(255) NOT NULL,
	`EMAIL` VARCHAR(50),
	`PHONE` VARCHAR(40),
     PRIMARY KEY(`ID_EVENT`),
	 CONSTRAINT `fk_client_id` FOREIGN KEY (`ID_EVENT`) REFERENCES `EVENTS` (`ID_EVENT`)
